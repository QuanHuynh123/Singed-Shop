package com.Singedshop.dto;

public class DashBoardDTO {
	public int Month ; 
	public int Quantity;
	public float Scale ; 
	
	public float getScale() {
		return Scale;
	}
	public void setScale(float scale) {
		this.Scale = scale;
	}
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	}
	public int getQuantity() {
		return Quantity;
	}
	public void setQuantity(int quantity) {
		Quantity = quantity;
	} 
	
	

}
